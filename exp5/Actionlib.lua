

function req_actionLUA()
    status, table = sensorReq("REQ",{ 0 },200)
    if status ~= 0 then
        return -255
    end
    return table[1]
end

function act_startLUA()
    status, table = sensorReq("START",{ 0 },1)
    if status ~= 0 then
        return -255
    end
    return table[1]
end


function act_completeLUA()
    status, table = sensorReq("COMPLETE",{ 0 },1)
    if status ~= 0 then
        return -255
    end
    return table[1]
end

function act_errorLUA()
    status, table = sensorReq("ERROR",{ 0 },500)
    if status ~= 0 then
        return -255
    end
    return table[1]
end

function return_test(nametest)
    status, table = sensorReq("REtest",{ nametest },500)
    if status ~= 0 then
        return -255
    end
    return table[1]
end

